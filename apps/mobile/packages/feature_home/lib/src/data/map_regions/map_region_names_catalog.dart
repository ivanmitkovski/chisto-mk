bool mapRegionHasAllLocaleNames(String id) =>
    _mapRegionNamesEn.containsKey(id) &&
    _mapRegionNamesMk.containsKey(id) &&
    _mapRegionNamesSq.containsKey(id);

String? mapRegionNameForLocale({
  required String id,
  required String localeName,
}) {
  final String language = localeName.toLowerCase().split('_').first;
  switch (language) {
    case 'mk':
      return _mapRegionNamesMk[id];
    case 'sq':
      return _mapRegionNamesSq[id];
    default:
      return _mapRegionNamesEn[id];
  }
}

